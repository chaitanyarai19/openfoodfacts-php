<?php

namespace OpenFoodFacts\Document;

use OpenFoodFacts\Document;

class BeautyDocument extends Document
{
}

    /**
     * This function is used to subtract two numbers 
     * @param int|float $a first number
     * @param int|float $b second number
     * @return int|float the result of the subtraction
     */
    function subtract($a, $b) {
        return $a - $b;
    }

    /**
     * This function is used to multiply two numbers 
     * @param int|float $a first number
     * @param int|float $b second number
     * @return int|float the result of the multiplication
     */
    function multiply($a, $b) {
        return $a * $b;
    }